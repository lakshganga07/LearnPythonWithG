import os
import sys

def main():
    # Arithmetic
    x = 10
    y = 3
    print(x + y)  # 13
    print(x - y)  # 7
    print(x * y)  # 30
    print(x / y)  # 3.33

    # Comparison
    print(x > y)  # True
    print(x == y)  # False

    # Logical
    print(x > 5 and y < 5)  # True

if __name__ == '__main__':
    main()